package Arrays;

public class Pro2 {
	public static void main(String args[]) {
		System.out.println("for each loop:");
		int arr[]= {33,3,4,5};
		for(int i:arr) {
			System.out.println(i);
		}
		System.out.println("==============");
		System.out.println("for loop:");
		int a[]= {44,4,5,6};
		for(int j=0;j<a.length;j++) {
			System.out.println(a[j]);
		}
	}

}
